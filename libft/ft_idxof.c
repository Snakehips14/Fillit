#include "libft.h"

int	ft_idxof(const char *s, char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s[j])
		j++;
	while (i <= j)
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
